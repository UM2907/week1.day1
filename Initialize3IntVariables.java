package week1.day1;

public class Initialize3IntVariables {
	public static void main(String[] args) {
		
		int a=0, b=1,sum;
		System.out.println(a);
		
		for (int c=1; c<=8; c++)
		{
			System.out.println(c);
		}
		
		sum=a+b;
		a=b;
		b=sum;
		
		System.out.println(sum);
		
	}

}
